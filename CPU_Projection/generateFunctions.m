N = inv(M);
N11 = N(1,1);
N12 = N(1,2);
N13 = N(1,3);
N14 = N(1,4);
N22 = N(2,2);
N23 = N(2,3);
N24 = N(2,4);
N33 = N(3,3);
N34 = N(3,4);
N44 = N(4,4);
matlabFunction(N11,N12,N13,N14,N22,N23,N24,N33,N34,N44,'file','invertSymmetric4x4_autogenerated.m','vars',[m11 m12 m13 m14 m22 m23 m24 m33 m34 m44]);